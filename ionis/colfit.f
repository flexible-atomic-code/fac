c     iz - atomic number 
c     in - number of electrons from 1 to iz 
c     is - shell number 
c     output:
c     e, ionization threshold in eV.
ccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccc
      integer iz, in, is
