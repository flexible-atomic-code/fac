     &4.9432e+13,5.7760e+13,3.0225e+13,3.3641e+12 /
      if(x.gt.0.27)then
        p=0.0
        q=0.0
        do i=1,15
          p=p+(pa(i)/(x**(i-1)))
          q=q+(qa(i)/(x**(i-1)))
          fcl2=((p/q)/x)/x
        enddo
      else 
        fcl2=(((dlog(x)**2)/2.0)+(0.57722*dlog(x)))+1.0
      endif
      return 
      end
