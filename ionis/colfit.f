      subroutine colfit(iz,in,is,t,ca,c)
* Version 4.
* January 8, 1997. Minor modification (exp are calculated with
* double precision) which improves accuracy of very low rates 
* (less than 10**(-20)) to avoid negative Na+ rates at logT < 4.2
* November 12, 1996. Improved presentation for the first exponential integral
******************************************************************************
*** This subroutine calculates partial or total rates of direct collisional
*** ionization, including excitation-autoionization contribution, for all 
*** ionization stages of all elements from H to Zn (Z=30)
*** in the range 10^4 - 10^9 K by use of the fits from
*** M. Arnaud and J. Raymond, 1992, ApJ 398, 394 for Fe, from 
*** M. Arnaud and R. Rothenflug, 1985, A&AS 60, 425 for H, He, C, N, O, Ne,
*** Na, Mg, Al, Si, S, Ar, Ca, Ni (with some corrections described in
*** Verner & Yakovlev, 1990, ApSS, 165, 27), and from 
*** interpolation/extrapolation for other elements.
*** Input parameters:  iz - atomic number 
***                    in - number of electrons from 1 to iz 
***                    is - shell number 
***                    t  - temperature, K
*** Output parameter:  c  - rate coefficient, cm^3 s^(-1)
*** If the input parameter is=0, the subroutine calculates total rate,
*** otherwise the subroutine calculates partial rate for the corresponding
*** shell: 1 - 1s, 2 - 2s, 3 - 2p, 4 - 3s, 5 - 3p, 6 - 3d, 7 - 4s.
*** Autoionization contribution is added to the rate for the outermost shell.
*** Fits are available for 3 (or less) outer shells. In other cases, 
*** the subroutine returns c=0. 
******************************************************************************
      common/clion/clion(5,30,30,7)
      common/eafe/eafe(6,26)
      real*8 x,y,fcl1,fcl2, t, c, ca
      integer iz, in, is
      c=0.0
      ca = 0.0
      if(iz.lt.1.or.iz.gt.30)return
      if(in.lt.1.or.in.gt.iz)return
      if(in.lt.5)ismin=1
      if(in.gt.4.and.in.lt.13)ismin=2
      if(in.gt.12.and.in.lt.29)ismin=4
      if(in.gt.28.or.(iz.eq.in.and.iz.gt.20).or.((iz-in).eq.1.and.
     &(iz.eq.22.or.iz.eq.25.or.iz.eq.26)))ismin=5
      ismax=ismin+2
      if(in.lt.3)ismax=1
      if((in.gt.2.and.in.lt.11).or.(in.gt.12.and.in.lt.19))
     &ismax=ismin+1
      if((in.eq.19.and.iz.gt.18.and.iz.lt.21).or.(in.eq.20.
     &and.iz.eq.20))ismax=ismin+3
      imin=ismin
      imax=ismax
      if(is.ne.0) then
         if(is.lt.imin.or.is.gt.imax) then
            return
         else
            imin=is
            imax=is
         endif
      endif
      tk=8.6171e-05*t
      do i=imin,imax
         x=clion(1,iz,in,i)/tk
         if(x.le.30.0)c=c+((dexp(-x)/x)*((((clion(2,iz,in,i)*
     &(1.0-(x*fcl1(x))))+(clion(3,iz,in,i)*((1.0+x)-((x*(x+2.0))
     &*fcl1(x)))))+(clion(4,iz,in,i)*fcl1(x)))+((clion(5,iz,in,i)
     &*x)*fcl2(x))))
      enddo
      c=6.69e-07*c/tk/sqrt(tk)
      if(is.ne.0.and.is.lt.ismax)return
*** autoionization rates
      ct=6.69e+07/sqrt(tk)
      if(iz.eq.26)then
         if((in.gt.10.and.in.lt.25).or.in.eq.3)then
            y=eafe(1,in)/tk
            if(y.lt.50.0) ca=ct*dexp(-y)*(eafe(2,in)+eafe(3,in)*
     &(1.0-y*fcl1(y))+eafe(4,in)*(1.0-y*(1.0-y*fcl1(y)))+eafe(5,in)*
     &(1.0-0.5*(y-y*y+y**3*fcl1(y)))+eafe(6,in)*fcl1(y))*1.0e-16
         endif
      else
      if(in.eq.3)then
         ea=13.6*((iz-0.835)**2-0.25*(iz-1.62)**2)
         y=ea/tk
         if(y.lt.50.0)then
            b=1.0/(1.0+2.0e-04*iz**3)
            g=2.22*fcl1(y)+0.67*(1.0-y*fcl1(y))+0.49*y*fcl1(y)+
     &1.2*y*(1.0-y*fcl1(y))
            ca=1.92e-07*b/(iz-0.43)**2/sqrt(tk)*dexp(-y)*g
            if(iz.eq.6)ca=ca*0.6
            if(iz.eq.7)ca=ca*0.8
            if(iz.eq.8)ca=ca*1.25
         endif
      endif
      if(in.eq.11)then
         if(iz.lt.17) then
            ea=26.0*(iz-10)
            y=ea/tk
            dz=(iz-11)**0.7
            if(iz.eq.11)dz=1.0
            if(y.lt.50.0)ca=2.8e-17/dz*ct*ea*dexp(-y)*(1.0-y*fcl1(y))
         else
            ea=11*(iz-10)*sqrt(iz-10.0)
            y=ea/tk
            if(y.lt.50.0)ca=1.3e-14/(iz-10)**3.73*ct*ea*dexp(-y)
     &*(1.0-0.5*(y-y*y+y**3*fcl1(y)))
         endif          
      endif
      if(in.gt.11.and.in.lt.17) then
         if(in.eq.12)ea=10.3*(iz-10)**1.52
         if(in.eq.13)ea=18.0*(iz-11)**1.33
         if(in.eq.14)ea=18.4*(iz-12)**1.36
         if(in.eq.15)ea=23.7*(iz-13)**1.29
         if(in.eq.16)ea=40.1*(iz-14)**1.10
         y=ea/tk
         if(y.lt.50.0)ca=4.0e-13/(iz*iz)*ct*dexp(-y)*(1.0-0.5*
     &(y-y*y+y**3*fcl1(y)))
      endif
      if(iz.eq.20.and.in.eq.20)then
         ea=25.0
         ab=6.0e-17
         ba=1.12
         y=ea/tk
         if(y.lt.50.0)ca=ct*ab*ea*dexp(-y)*(1.0+ba*fcl1(y))
      endif
      if(iz.eq.20.and.in.eq.19)then
         ea=29.0
         ab=9.8e-17
         ba=1.12
         y=ea/tk
         if(y.lt.50.0)ca=ct*ab*ea*dexp(-y)*(1.0+ba*fcl1(y))
      endif
      endif

      return
      end

      subroutine ccolfit(iz,in,is,t,ca,c)
* This calculates the cross section.
* Version 4.
* January 8, 1997. Minor modification (exp are calculated with
* double precision) which improves accuracy of very low rates 
* (less than 10**(-20)) to avoid negative Na+ rates at logT < 4.2
* November 12, 1996. Improved presentation for the first exponential integral
******************************************************************************
*** This subroutine calculates partial or total rates of direct collisional
*** ionization, including excitation-autoionization contribution, for all 
*** ionization stages of all elements from H to Zn (Z=30)
*** in the range 10^4 - 10^9 K by use of the fits from
*** M. Arnaud and J. Raymond, 1992, ApJ 398, 394 for Fe, from 
*** M. Arnaud and R. Rothenflug, 1985, A&AS 60, 425 for H, He, C, N, O, Ne,
*** Na, Mg, Al, Si, S, Ar, Ca, Ni (with some corrections described in
*** Verner & Yakovlev, 1990, ApSS, 165, 27), and from 
*** interpolation/extrapolation for other elements.
*** Input parameters:  iz - atomic number 
***                    in - number of electrons from 1 to iz 
***                    is - shell number 
***                    t  - temperature, K
*** Output parameter:  c  - rate coefficient, cm^3 s^(-1)
*** If the input parameter is=0, the subroutine calculates total rate,
*** otherwise the subroutine calculates partial rate for the corresponding
*** shell: 1 - 1s, 2 - 2s, 3 - 2p, 4 - 3s, 5 - 3p, 6 - 3d, 7 - 4s.
*** Autoionization contribution is added to the rate for the outermost shell.
*** Fits are available for 3 (or less) outer shells. In other cases, 
*** the subroutine returns c=0. 
******************************************************************************
      common/clion/clion(5,30,30,7)
      common/eafe/eafe(6,26)
      real*8 x,y,fcl1,fcl2, t, c, ca
      integer iz, in, is
      c=0.0
      ca = 0.0

      if(iz.lt.1.or.iz.gt.30)return
      if(in.lt.1.or.in.gt.iz)return
      if(in.lt.5)ismin=1
      if(in.gt.4.and.in.lt.13)ismin=2
      if(in.gt.12.and.in.lt.29)ismin=4
      if(in.gt.28.or.(iz.eq.in.and.iz.gt.20).or.((iz-in).eq.1.and.
     &(iz.eq.22.or.iz.eq.25.or.iz.eq.26)))ismin=5
      ismax=ismin+2
      if(in.lt.3)ismax=1
      if((in.gt.2.and.in.lt.11).or.(in.gt.12.and.in.lt.19))
     &ismax=ismin+1
      if((in.eq.19.and.iz.gt.18.and.iz.lt.21).or.(in.eq.20.
     &and.iz.eq.20))ismax=ismin+3
      imin=ismin
      imax=ismax
      if(is.ne.0) then
         if(is.lt.imin.or.is.gt.imax) then
            return
         else
            imin=is
            imax=is
         endif
      endif

      tk = t
      do i=imin,imax
         x=tk/clion(1,iz,in,i)
         if (x .ge. 1.0) then
            y = clion(2,iz,in,i)*(1-1/x) +
     &           clion(3,iz,in,i)*(1-1/x)**2 +
     &           (clion(4,iz,in,i)+clion(5,iz,in,i)/x)*dlog(x)
            y = y * 1e6 / (x*clion(1,iz,in,i)**2)
            c = c + y
         endif
      enddo

      if(is.ne.0.and.is.lt.ismax)return
*** autoionization cross section, for iron only
      if(iz.eq.26)then
         if((in.gt.10.and.in.lt.25).or.in.eq.3)then
            x = tk/eafe(1,in)
            if (x .ge. 1.0) then
               y = eafe(2,in) + eafe(3,in)*(1-1/x) +
     &              eafe(4,in)*(1-1/x**2) +
     &              eafe(5,in)*(1-1/x**3) + eafe(6,in)*dlog(x)
               ca = y * 1e4 / (x*eafe(1,in))
            endif
         endif
      endif
      
      return
      end
      double precision function fcl1(x)
      real*8 x
      dimension a(6),b(8)
      data a/-0.57721566,0.99999193,-0.24991055,
     &0.05519968,-0.00976004,0.00107857/
      data b/8.5733287401,18.0590169730,8.6347608925,0.2677737343,
     &9.5733223454,25.6329561486,21.0996530827,3.9584969228/
      if(x.lt.1.0)then
         aa=(a(1)+a(2)*x+a(3)*x*x+a(4)*x**3+a(5)*x**4+a(6)*x**5-
     &dlog(x))*dexp(x)
      else
         if(x.lt.1.0e9)then
            aa=(x**4+b(1)*x**3+b(2)*x**2+b(3)*x+b(4))/(x**4+b(5)*x**3+
     &b(6)*x**2+b(7)*x+b(8))/x
         else
            aa=1.0/x
         endif   
      endif
      fcl1=aa
      return 
      end
      double precision function fcl2(x)
      real*8 x
      dimension pa(15),qa(15)
      data pa/1.0000e+00,2.1658e+02,2.0336e+04,1.0911e+06,3.7114e+07, 
     &8.3963e+08,1.2889e+10,1.3449e+11,9.4002e+11,4.2571e+12,1.1743e+13, 
     &1.7549e+13,1.0806e+13,4.9776e+11,0.0000e+00 /
      data qa/1.0000e+00,2.1958e+02,2.0984e+04,1.1517e+06,4.0349e+07, 
