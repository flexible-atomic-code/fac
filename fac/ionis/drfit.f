         r = r + drc(3,i)*exp(-dre(3,i)/t)
      endif
      if (drc(4,i) .gt. 0) then
         r = r + drc(4,i)*exp(-dre(4,i)/t)
      endif
      
      r = r/(t**(1.5))

      return
      end
      

