      subroutine drfit(iz, in, t, r)
      integer iz, in, i
      integer ind(28)
      real*8 t, r
      common /dr/dre(4, 378), drc(4, 378)
      
      data ind(1)/-1/
      save ind

      r = 0.0
      if (iz .lt. 2 .or. iz .gt. 28) then
         return
      endif

      if (ind(1) .lt. 0) then
         ind(1) = 0
         ind(2) = 1
         do i = 2, 27
            ind(i+1) = ind(i) + i - 1
         enddo
      endif

      i = ind(iz) + iz - in - 1
      if (drc(1,i) .gt. 0) then
         r = r + drc(1,i)*exp(-dre(1,i)/t)
      endif
      if (drc(2,i) .gt. 0) then
         r = r + drc(2,i)*exp(-dre(2,i)/t)
      endif
