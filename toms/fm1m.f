      double precision function fm1m(x)
      implicit real*8 (a-h, o-z)
      dimension a1(12), b1(12), a2(12), b2(12)
      data an, m1, k1, m2, k2/-0.5d0, 7, 7, 11, 11/
      data a1(1)/1.71446374704454d7/
      data a1(2)/3.88148302324068d7/
      data a1(3)/3.16743385304962d7/
      data a1(4)/1.14587609192151d7/
      data a1(5)/1.83696370756153d6/
      data a1(6)/1.14980998186874d5/
      data a1(7)/1.98276889924768d3/
      data a1(8)/1.0d0/
      data b1(1)/9.67282587452899d6/
      data b1(2)/2.87386436731785d7/
      data b1(3)/3.26070130734158d7/
      data b1(4)/1.77657027846367d7/
      data b1(5)/4.81648022267831d6/
      data b1(6)/6.13709569333207d5/
      data b1(7)/3.13595854332114d4/
      data b1(8)/4.35061725080755d2/
      data a2(1)/-4.46620341924942d-15/
      data a2(2)/-1.58654991146236d-12/
      data a2(3)/-4.44467627042232d-10/
      data a2(4)/-6.84738791621745d-8/
      data a2(5)/-6.64932238528105d-6/
      data a2(6)/-3.69976170193942d-4/
      data a2(7)/-1.12295393687006d-2/
      data a2(8)/-1.60926102124442d-1/
      data a2(9)/-8.52408612877447d-1/
      data a2(10)/-7.45519953763928d-1/
      data a2(11)/2.98435207466372d0/
      data a2(12)/1.0d0/
      data b2(1)/-2.23310170962369d-15/
      data b2(2)/-7.94193282071464d-13/
      data b2(3)/-2.22564376956228d-10/
      data b2(4)/-3.43299431079845d-8/
      data b2(5)/-3.33919612678907d-6/
      data b2(6)/-1.86432212187088d-4/
      data b2(7)/-5.69764436880529d-3/
      data b2(8)/-8.34904593067194d-2/
      data b2(9)/-4.78770844009440d-1/
      data b2(10)/-4.99759250374148d-1/
      data b2(11)/1.86795964993052d0/
      data b2(12)/4.16485970495288d-1/

      if (x .lt. 2d0) then
         xx=dexp(x)
         rn=xx+a1(m1)
         do i=m1-1,1,-1
            rn=rn*xx+a1(i)
         enddo
         den=b1(k1+1)
         do i=k1,1,-1
            den = den*xx+b1(i)
         enddo
         fm1m = xx*rn/den
      else
         xx=1d0/x**2
         rn=xx+a2(m2)
         do i=m2-1,1,-1
            rn = rn*xx+a2(i)
         enddo
         den=b2(k2+1)
         do i=k2,1,-1
            den=den*xx+b2(i)
         enddo
         fm1m = (x**(an+1d0))*rn/den
      endif
      return
      end
      
